      <section class="jumbotron section-orange-gradient">
        <h1 class="display-4 font-weight-bolder">How are Filipino Children Abused and Exploited Online? </h1>
        <hr class="my-4 hr-orange-short ml-0">
        <p>The National Study on Child Online Sexual Exploitation and Abuse found that in many cases, poverty was the
          primary motivation for children and families to get into this situation. Older children are promised education
          or money by foreigners who want their sexual pictures or videos. Younger children are lured into pedophiles’
          homes with candies and chocolates. </p>
        <h2 class="mt-5">Perpetrators</h2>
        <p class="font-weight-bold">(Pedophiles, who are usually from foreign countries)</p>
        <p>Building the trust and confidence of the children, fostering friendship and nurturing a relationship with
          them is what you call “grooming.” This grooming is done by “perpetrators” online, those that pay for the
          videos or pictures of children to be shared and sold on the internet. </p>
        <h2 class="mt-5">Facilitators</h2>
        <p class="font-weight-bold">(Those who procure children to do sexual acts online, usually parents or relatives)
        </p>
        <p>Then, there are those people who are close to the children who force or convince them to undress, perform
          sexual acts, rape or torture them in front of the web camera, and get the money. They are called the
          “facilitators.” They could be a child’s parents, relatives or neighbors. </p>
        <p>This is a hidden crime, unknown to many but experienced more and more by Filipino children in their own homes
          and in their own families.</p>
